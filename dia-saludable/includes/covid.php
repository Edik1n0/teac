                <h3>Lávate muy bien las manos</h3>

                <div class="wp-block-image">
                    <img src="http://teacompano.com.co/wp-content/uploads/2020/03/lavado-manos-blog-teacompano.jpg" alt="" />
                    <p>
                        Es el dato más valioso e importante que puedas recibir durante esta época de Cuarentena.
                        ¿Si todos te lo dicen a diario (incluyendo los medios masivos), no deberías hacer caso? Sigue estos consejos
                        para que mantengas tus manos limpias y a ti y a tu familia lejos del virus. La OMS (Organización Mundial de la Salud)
                        recomienda seguir los siguientes pasos:
                    </p>
                </div>

                <ul>
                    <li>Mojarse las manos</li>
                    <li>Aplicar suficiente jabón para cubrir toda la mano</li>
                    <li>Frotar las palmas entre si</li>
                    <li>Frotar la palma de la mano derecha contra el dorso de la mano izquierda entrelazando los dedos, y viceversa</li>
                    <li>Frotar las palmas de las manos entre sí, con los dedos entrelazados</li>
                    <li>Frotar el dorso de los dedos de una mano contra la palma de la mano opuesta, manteniendo unidos los dedos</li>
                    <li>Rodeando el pulgar izquierdo con la palma de la mano derecha, frotarlo con un movimiento de rotación, y viceversa.</li>
                    <li>Frotar la punta de los dedos de la mano derecha contra la palma de la mano izquierda, haciendo un movimiento de rotación, y viceversa.</li>
                    <li>Enjuagar las manos.</li>
                    <li>Secarlas con una toalla de un solo uso.</li>
                    <li>Utilizar la toalla para cerrar el grifo.</li>
                </ul>

                <p>
                    El correcto lavado de manos traerá beneficios importantes como reducir la aparición de otras enfermedades que pueden afectar
                    más comúnmente a niños y personas adultas, disminuye potencialmente la aparición de enfermedades respiratorias y <em>Minimiza la
                        incidencia de enfermedades cutáneas, de infecciones a los ojos como tracoma y de infecciones intestinales parasitarias como
                        ascariasis y trichuriasis</em> (Global Handwashing Partnership, 2008).
                </p>

